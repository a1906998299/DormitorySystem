package cn.edu.guet.bean;

public class Water {
    private String buid;
    private float wprice;
    private int wnum;

    public String getBuid() {
        return buid;
    }

    public void setBuid(String buid) {
        this.buid = buid;
    }

    public float getWprice() {
        return wprice;
    }

    public void setWprice(float wprice) {
        this.wprice = wprice;
    }

    public int getWnum() {
        return wnum;
    }

    public void setWnum(int wnum) {
        this.wnum = wnum;
    }
}
